#!/bin/sh

set -x

# =============================================================================
# Variables
# =============================================================================

export VLC_PID=$$
export VLC_BINARY=cvlc
export VLC_ARGUMENTS=--play-and-exit

LOCAL_FILE="$1"

# =============================================================================
# Functions
# =============================================================================

# None

# =============================================================================
# Main
# =============================================================================

if [ $# -eq 1 ]
then
    $VLC_BINARY $VLC_ARGUMENTS $LOCAL_FILE
else
    echo "Invalid number of arguments, see Documentation"
    exit 1
fi

# End of File
